				Recent Content Changes				<div class="date"><img src="images/icons/date.png"> Dec <b>30</b>, &#39;10</div>
			</div>
			<div class="body">

				Today I've made the another step towards GMT 2.0.<br />
<br />
I've gone ahead an made some major changes to the way we'll be pushing out content for GMod Tower and its gamemodes.<br />
<br />
<div align="center" style="margin-bottom: 5px;"><img src="thumb/phpThumb.php?src=http://img.macdguy.org/i/zs-30-12-2010_01.23.43.png&w=700&h=500&fltr[]=ric|10|10&f=png" alt="http://img.macdguy.org/i/zs-30-12-2010_01.23.43.png"></div><br />
<br />
As you can see, the tower addon has been split up into separate addons pertaining to the gamemodes.<br />
<br />
<br />
<br />
<div align="center"><h2><span style="color: #FF0000;">FOR THOSE WHO INSTALLED THE CONTENT MANUALLY (either by SVN or ZIP), PLEASE COMPLETELY REMOVE THE FORMER 'GMOD TOWER' ADDON AND REINSTALL THE NEW ONE</span></h2></div><br />
<br />

<br />
<div style="margin: 4px; float: left;"><div align="center"><img src="http://img.macdguy.org/i/zs-30-12-2010_01.39.05.png" alt="http://img.macdguy.org/i/zs-30-12-2010_01.39.05.png"></div></div> GMT2_Base should always be loaded in order to play GMod Tower related gamemodes.  Specifically this contains player models, hats, GUI, and some sounds used in each gamemode.<br />
<br />
GMT2_Lobby is obviously all the content needed to play on our Lobby server.  I suggest having this loaded at all times as well.<br />
<br />
And GMT2_BallRace, GMT2_PVP, GMT2_UCH, and GMT2_Virus is all the content for each gamemode.  You can unload these if you want, but because of some shared content (milestones, for example) I'd leave those on.<br />
<br />
<br />
Why was this change made?  Because going forward this is the easiest way for us to manage content for the increasing number of gamemodes coming out.  I can pack up gamemode content easily and just update individual packs when needed.  It's also much more organized.  Oh and there's icons.<br />
<br />
<br />
Now what's included in this update other than the structure change, you ask?<br />

Well...<br />
<br />
<div align="center"><h2>New Furniture</h2></div><br />
<div align="center" style="margin-bottom: 5px;"><img src="thumb/phpThumb.php?src=http://img.macdguy.org/i/zs-30-12-2010_00.12.48.jpg&w=700&h=500&fltr[]=ric|10|10&f=png" alt="http://img.macdguy.org/i/zs-30-12-2010_00.12.48.jpg"></div><br />
Coffee Table, Comfy Bed, Comfy Chair, Kitchen Table, Desk Lamp, Table Lamp, Medium Chair, Medium Desk, and Medium Desk Corner.<br />
<br />
Credits go to the original model creators.<br />
<br />
<br />
<div align="center"><h2>New Ball Race Orbs</h2></div><br />
<div align="center" style="margin-bottom: 5px;"><img src="thumb/phpThumb.php?src=http://img.macdguy.org/i/zs-30-12-2010_00.14.35.jpg&w=700&h=500&fltr[]=ric|10|10&f=png" alt="http://img.macdguy.org/i/zs-30-12-2010_00.14.35.jpg"></div><br />
Spiked Ball, Soccer Ball, Geo Ball, and Bomb Ball.<br />

<br />
Thanks to Sam for the new models.<br />
<br />
<br />
<br />
I also went through and saved a ton of memory usage on textures by lowering resolutions on our content.  Before GMT was using around 1.3GB (ouch) of memory when loaded.  Now it's around 700MB.  Totally an improvement and will help with crashes and load times.<br />
<br />
I am working on getting new arcade cabinets, retro arcades, new UCH and virus maps (created by Lifeless), new posters, iMac computers, and a load of other new content and goodies out for you.<br />
<br />
<br />
Nican went and updated the GMT scoreboard with avatars and icons.  Icons are still being developed and when they're fully released/supported - you'll be able to pay (PayPal) for custom icons and trade them as inventory items.<br />
<br />
<div align="center" style="margin-bottom: 5px;"><img src="thumb/phpThumb.php?src=http://img.macdguy.org/i/zs-30-12-2010_01.58.30.png&w=700&h=500&fltr[]=ric|10|10&f=png" alt="http://img.macdguy.org/i/zs-30-12-2010_01.58.30.png"></div><br />

<br />
<br />
I'm currently working on the new suite floorplan and I'm looking for opinions on what you want for the new suite.  So please if you can, <a href="http://www.gmtower.org/forums/showthread.php?t=6727">stop by the forums</a> and share your ideas.<br />
<br />
<br />
On a final note, Voided is fully back as a developer for GMT and is working on a number of awesome stuff for the tower lobby and new gamemodes.<br />
<br />
Whew.  We also hope everyone had a wonderful holiday season!  GMT will remain on the holiday-themed map until the 2nd of January.			</div>
